# Ukážka aritmetických, porovnávacích a logických operátorov

a = 10
b = 3

# Aritmetické operátory
print("Súčet:", a + b)
print("Rozdiel:", a - b)
print("Súčin:", a * b)
print("Podiel:", a / b)
print("Celočíselné delenie:", a // b)
print("Zvyšok po delení:", a % b)
print("Mocnina:", a ** b)

# Porovnávacie operátory
print("a == b:", a == b)
print("a > b:", a > b)
print("a < b:", a < b)

# Logické operátory
x = True
y = False
print("x and y:", x and y)
print("x or y:", x or y)
print("not x:", not x)
