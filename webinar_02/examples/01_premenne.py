# Príklady práce s premennými a dátovými typmi

vek = 35
meno = "Ján"
pi = 3.14159
je_programator = True

print("Meno:", meno)
print("Vek:", vek)
print("Číslo pi:", pi)
print("Je programátor?", je_programator)

# Funkcia type() ukáže dátový typ
print("Typ premennej vek:", type(vek))
print("Typ premennej meno:", type(meno))
