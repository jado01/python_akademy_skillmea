# Bonus: Zaobli podiel na 2 desatinne miesta pomocou round().

a = 10
b = 3

# sucet = a + b
# rozdiel = a - b
# sucin = a * b
# podiel = a / b

print(f"Sucet: {a + b}")
print(f"Rozdiel: {a - b}")
print(f"Sucin: {a * b}")
print(f"Podiel {round (a / b,2)}")