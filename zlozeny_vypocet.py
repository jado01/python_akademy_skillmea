# Priklad 5 - Zlozeny vypocet a logicka kontrola

# - Zadaj "x = 8.5", "y = 2.5"
# - Vypocitaj: "(x + y) * 2 - x / y"
# - Uloz vysledok do "vysledok"
# - Potom "je_vysledok_vacsi_ako_15 = vysledok > 15"
# - Vypis obe premenne
# - Uloz kod do suboru "zlozeny_vypocet.py"

x = 8.5
y = 2.5
vysledok = (x + y) * 2 - x / y
je_vysledok_vacsi_ako_15 = vysledok > 15
print(vysledok)
print(je_vysledok_vacsi_ako_15)

